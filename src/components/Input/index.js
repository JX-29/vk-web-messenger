function Input({sentMessage}) {

  //позволяет применять стили на текст, выводимый вместо инпута, так как инпут не поддерживает разметку содержимого. Позволяет сделать из выбранных выражений ссылки и, в зависимости от типа выражения, переходить по ним. Так же в случае если пользователь ввел html спецсимволы или теги, выводит их как верстку тоже (вместо <span>text</span> будет выводиться span). Можно удалить добавив регулярное выражение на поиск html-тегов, но может считаться и дополнительным функционалом, делая из инпута текстовый редактор

  const layoutText = (text) => {
    let layout = text
    const urlRegex = /https?:\/\/(www\.)?[-a-zA-Z0-9@:%._\+~#=]{1,256}\.[a-zA-Z0-9()]{1,6}\b([-a-zA-Z0-9()@:%_\+.~#?&//=]*)?/gi;
    const emailRegex = /\b([a-z0-9._-]+@[a-z0-9.-]+)\b/gi;
    const tagRegex = /(?:#)([A-Za-z0-9_](?:(?:[A-Za-z0-9_]|(?:\.(?!\.))){0,28}(?:[A-Za-z0-9_]))?)/gi;
    const twitterRegex = /(?<=^|(?<=[^a-zA-Z0-9-_\.]))@([A-Za-z]+[A-Za-z0-9-_]+)/gi;

    if (text.match(urlRegex) && text.match(urlRegex).length) {
      text.match(urlRegex).map((url) => {
        layout = layout.replace(url, `<a target="_blank" href=${url}>${url}</a>`)
      })
    }
    if (text.match(emailRegex) && text.match(emailRegex).length) {
      text.match(emailRegex).map((email) => {
        layout = layout.replace(email, `<a target="_blank" href=${'mailto:'+email}>${email}</a>`)
      })
    }
    if (text.match(twitterRegex) && text.match(twitterRegex).length) {
      text.match(twitterRegex).map((twitter) => {
        layout = layout.replace(twitter, `<a target="_blank" href=${'https://twitter.com/'+twitter.replace('@', '')}>${twitter}</a>`)
      })
    }
    if (text.match(tagRegex) && text.match(tagRegex).length) {
      text.match(tagRegex).map((tag) => {
        layout = layout.replace(tag, `<a target="_blank" href=${'https://www.instagram.com/explore/tags/'+tag.replace('#', '')}>${tag}</a>`)
      })
    }

    return layout

  }

  const [viewMarkup, setViewMarkup] = useState(true)
  const [showHint, setShowHint] = useState(true)
  const [message, setMessage] = useState(initialMessage)
  const [showEmojiPanel, setShowEmojiPanel] = useState(false)

  const field = useRef(null)

  const onShowEmojiPanel = () => {
    onChangeMessage()
    setShowEmojiPanel(!showEmojiPanel)
  }

  const onSelectEmoji = (emoji) => {
    onChangeMessage()
    if (showHint) {
      setShowHint(false)
    }
    if (!viewMarkup) {
      setMessage(`${field.current.innerText}${emoji}`)
    }
  }

  const onChangeMessage = () => {
    const value = field.current.innerText
    setMessage(value)
  }

  const publishMessage = () => {
    if (showHint) {
      setShowHint(false)
    }
    setViewMarkup(false)
    if (field.current.innerText) {
      sentMessage(layoutText(field.current.innerText))
      setMessage('')
    }
  }

  const onSwitchMode = () => {
    if (showHint) {
      setShowHint(false)
    }
    if (viewMarkup) {
      onChangeMessage()
      setViewMarkup(false)
    }
    if (!viewMarkup) {
      onChangeMessage()
      setViewMarkup(true)
    }
  }

  //при выборе текста может копироваться верстка, и, к сожалению, вставляться, поэтому эта функция получает содержимое из буфера обмена и вставляет его именно как текст, дабы избежать проблем с отображением
  const onPaste = (e) => {
    e.preventDefault()
    const pasteText = (e.clipboardData || window.clipboardData).getData('text')
    setMessage(pasteText)
  }

  return (
    <div className='input'>
      <div className={`input__switch-mode ${viewMarkup ? 'input__switch-mode_active' : 'input__switch-mode_default'}`} onClick={onSwitchMode}>
        {showHint && (
          <div className="input__hint">
            <Hint/>
          </div>
        )}
        {getTextIcon()}
      </div>
      <div className='input__wrapper'>
        {showEmojiPanel && (
          <div className='input__emoji-window'><Emoji setEmoji={onSelectEmoji} emoji={emoji}/></div>
        )}
        {/*сначала я попытался создать собственный элемент инпута с нуля, так как стандартный инпут не поддерживает разметку, но не хватило времени, так как нужно было учесть много ньюансов для корректной работы. */}
        {/*так же я попробовал сделать блок с текстом, полностью имитирующий textarea, но из-за разницы в управлении пришлось отказаться и от этого варианта*/}
        {/*в качестве окончательного варианта был выбран div-блок с contentEditable=true, но здесь возникают проблемы с управлением. При изменении текста каретка отправляется в начало текста, поэтому я изменил взаимодействие с инпутом и вместо полной зависимости от state сделал гибридную, где state обновляется после нажатий на кнопки (выбор смайла, отправка сообщения и т.д).  */}
        {viewMarkup ? (
          <div className={`input__field ${viewMarkup ? 'input__field_view' : 'input__field_default'}`} ref={field} dangerouslySetInnerHTML={{__html: layoutText(message)}} contentEditable={!viewMarkup} onPaste={onPaste}>
          </div>
        ) : (
          <div className={`input__field ${viewMarkup ? 'input__field_view' : 'input__field_default'}`} onPaste={onPaste} ref={field} contentEditable={!viewMarkup}>
            {message}
          </div>
        )}
        <div className='input__emoji' onClick={onShowEmojiPanel}>
          {getEmojiIcon()}
        </div>
      </div>
      <div className="input__sent" onClick={publishMessage}>{getSentIcon()}</div>
    </div>
  );
}

export default Input;
